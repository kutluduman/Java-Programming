package day11;

public class LogicalNotOperator {

    public static void main(String[] args) {


        // logical not operator ! exclamation mark is used to negate the boolean value

        // 1)

        System.out.println( true );
        System.out.println( false );

        System.out.println( !true );
        System.out.println( !false );


        // 2)

        boolean isRecording = false ;

        System.out.println( !isRecording); // made it true with the exclamation mark


        // 3)

        System.out.println(   !(7>5)   ); // when you compare numbers, put bracket and then expression to flip the
                                            // coin to see the boolean reverse outcome




    }


}
