package day11;

public class LogicalOperator_SinglePipe {

    public static void main(String[] args) {

        System.out.println( 10 > 5 || 9 / 0 == 3); // one is true so the rest is true too

        System.out.println( 10 > 5 | 9 / 0 == 3); // first one is true but it goes next because of | and it fails


        // || is boolean










    }


}
