package Recap;

public class RecapConcatenation {


    public static void main(String[] args) {


        String str = "any text goes here";

        System.out.println("str = " + str);

        String helloWorld = "Hello World";

        System.out.println("helloWorld = " + helloWorld);

        String myName = "Cybertek" + "School";

        System.out.println("myName = " + myName);

        String year = "This is " + 2020;

        System.out.println("year = " + year);

        String name = "Muhtar";

        System.out.println("My name is " + name);

        int cucumber = 3;
        int tomato = 5;

        System.out.println("Cucumber is " + cucumber + "$ and tomato is " + tomato + "$");

        String newStr = "100" + 10;

        System.out.println("newStr = " + newStr);

        System.out.println("1" + 2 + 3);

//        System.out.println("1"+1-3);// it wont compile because it's a text and - has no role

        System.out.println("Batch 12"+1+2);

        System.out.println(2-1+1+"4");

    }
}
