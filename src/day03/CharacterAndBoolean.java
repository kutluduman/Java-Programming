package day03;

public class CharacterAndBoolean {

    public static void main(String[] args) {


        char grade = 'A';
        char emailSign = '@';

        System.out.println("My Math score is " + grade);
        System.out.println("We use special character at emails which is " + emailSign);

        boolean isLightOn = true ;


        // changing from true to false or opposite


        System.out.println("Did you turn off the light? " + isLightOn) ;

        isLightOn = false ;

        System.out.println("Did you turn off the light? " + isLightOn) ;



        boolean areYouSleeping = false ;

        System.out.println("Are you sleeping? " + areYouSleeping);









    }

}
