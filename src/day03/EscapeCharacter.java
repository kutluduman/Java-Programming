package day03;

public class EscapeCharacter {
    public static void main(String[] args) {

        System.out.println("Hello\tworld") ;

        System.out.println("Hello\nworld") ;

        System.out.println("Hello\'world") ;

        System.out.println("Hello\"world") ;

        System.out.println("Hello\\world") ;

        System.out.println("My favorite book is \"Aglat Agaci\" ") ;

        System.out.println("Adam\'s world") ;

        System.out.println("My favorite movie is \"The Dark Knight\" ") ;

        System.out.println("I am\n BATMAN") ;


        // even though we have print and println, by adding \n, we add new line.
        System.out.print("This is regular print \n") ;
        System.out.print("This is println") ;


    }

}
